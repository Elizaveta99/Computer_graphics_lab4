    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        readCoord();
        coordx1.setEnabled(false);
        coordy1.setEnabled(false);
        coordx2.setEnabled(false);
        coordy2.setEnabled(false);
        long startTime = 0L, endTime = 0L;
        startTime = System.nanoTime();
        Algorithm algo = null;
        switch (type) 
        {
                case (1):
                    algo = new StepByStep(x1, y1, x2, y2);
                    break;
                case (2):
                    algo = new BresenhamLine(x1, y1, x2, y2);
                    break;
                case (3):
                    algo = new BresenhamCircle(x1, y1, r);
                    break;
                case (4):
                    algo = new DigitalDifferentialAnalyzer(x1, y1, x2, y2);
                    break;
        }
        
        algoPanel.addFigure(algo);
        endTime = System.nanoTime();
        timeField.setText(String.format("%d", (endTime - startTime) / 1000));  
    }//GEN-LAST:event_jButton1ActionPerformed


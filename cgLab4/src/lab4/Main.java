    private void readCoord()
    {
        try
        {
            x1 = Integer.parseInt(coordx1.getText());
        }
        catch (NumberFormatException e)
        {
            coordx1.setText(Integer.toString(exmpl_x1));
            x1 = exmpl_x1;
        }
        try
        {
            y1 = Integer.parseInt(coordy1.getText());
        }
        catch (NumberFormatException e)
        {
            coordy1.setText(Integer.toString(exmpl_y1));
            y1 = exmpl_y1;
        }
        try
        {
            x2 = Integer.parseInt(coordx2.getText());
        }
        catch (NumberFormatException e)
        {
            coordx2.setText(Integer.toString(exmpl_x2));
            x2 = exmpl_x2;
        }
        
        if (type != 3) 
        {
            try
            {
                y2 = Integer.parseInt(coordy2.getText());
            }
            catch (NumberFormatException e)
            {
                coordy2.setText(Integer.toString(exmpl_y2));
                y2 = exmpl_y2;
            }
        }
        else 
            r = x2;
        
    }
    
